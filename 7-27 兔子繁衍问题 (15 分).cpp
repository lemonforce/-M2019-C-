/*
一对兔子，从出生后第3个月起每个月都生一对兔子。
小兔子长到第3个月后每个月又生一对兔子。
假如兔子都不死，请问第1个月出生的一对兔子，
至少需要繁衍到第几个月时兔子总数才可以达到N对？
*/

#include <stdio.h>

int main() {
	int a[30] = { 1,1,2, };
	int i = 2;
	while (a[i-1] < 10001) {
		a[i] = a[i - 1] + a[i - 2];
		i++;
	}
	int n = 0;
	i = 0;
	scanf("%d", &n);
	while (a[i] < n) {
		i++;
	}
	printf("%d", i + 1);
	return 0;
}