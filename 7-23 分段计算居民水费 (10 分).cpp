/*
为鼓励居民节约用水，自来水公司采取按用水量阶梯式计价的办法，居民应交水费y（元）与月用水量x（吨）相关：
当x不超过15吨时，y=4x/3；超过后，y=2.5x−17.5。请编写程序实现水费的计算。
*/
#include <stdio.h>

int main() {
	double x = 0.0, y = 0.0;
	scanf("%lf", &x);
	if (x <= 15.0){
		y = 4.0 * x / 3.0;
	}
	else {
		y = 2.5 * x - 17.5;
	}
	printf("%.2lf\n", y);
	return 0;
}